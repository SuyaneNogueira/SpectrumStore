// üîπ 4. ROTAS STRIPE
// =========================================================
app.post("/create-checkout-session", async (req, res) => {
  const { cartItems, paymentMethod } = req.body;

  try {
    const paymentTypes = paymentMethod === "pix" ? ["pix"] : ["card"];

    const session = await stripe.checkout.sessions.create({
      payment_method_types: paymentTypes,
      mode: "payment",
      
      // <--- ADICIONADO: Coleta de e-mail obrigat√≥ria
      customer_email_collection: { enabled: true },
      // ---> FIM DA ADI√á√ÉO

      line_items: cartItems.map((item) => ({
        price_data: {
          currency: "brl",
          product_data: {
            name: item.name,
            images: item.image ? [item.image] : [],
          },
          // <--- MELHORADO: Garante um valor m√≠nimo (ex: R$ 0,50) para evitar erros
          unit_amount: Math.max(Math.round(item.price * 100), 50),
          // ---> FIM DA MELHORIA
        },
        quantity: item.quantity || item.quantidade || 1,
      })),
      success_url: "http://localhost:5173/sucesso?session_id={CHECKOUT_SESSION_ID}",
      cancel_url: "http://localhost:5173/cancelado",
    });

    res.json({ url: session.url });
  } catch (err) {
    console.error("‚ö†Ô∏è Erro Stripe:", err);
    res.status(500).json({ error: err.message });
  }
});

app.get("/checkout-session/:sessionId", async (req, res) => {
  const { sessionId } = req.params;
  try {
    const session = await stripe.checkout.sessions.retrieve(sessionId, {
      expand: ["line_items.data.price.product"],
    });
    res.json(session);
  } catch (err) {
    console.error("‚ùå Erro ao buscar sess√£o:", err);
    res.status(500).json({ error: err.message });
  }
});

