import express from "express";
import cors from "cors";
import Stripe from "stripe";
import bodyParser from "body-parser";
import dotenv from "dotenv";
import { pool } from "./db.js";
import { defineRoutes } from "./CarrinhoBackT.js";

dotenv.config();

const app = express();
const PORT = process.env.PORT || 3001;
const stripe = new Stripe(process.env.STRIPE_SECRET_KEY);

// =========================================================
// üîπ 1. WEBHOOK STRIPE (vem ANTES do express.json)
// (Deixamos este c√≥digo aqui para quando voc√™ for para produ√ß√£o)
// =========================================================
app.post(
  "/webhook",
  bodyParser.raw({ type: "application/json" }),
  async (req, res) => {
    const sig = req.headers["stripe-signature"];

    try {
      const event = stripe.webhooks.constructEvent(
        req.body,
        sig,
        process.env.STRIPE_WEBHOOK_SECRET
      );

      // üî∏ Quando o pagamento for conclu√≠do
      if (event.type === "checkout.session.completed") {
        const session = event.data.object;

        // <--- ADICIONADO: Verifica√ß√£o para evitar duplicidade
        // (Caso a p√°gina de sucesso j√° tenha salvado)
        const checkQuery = "SELECT id FROM pedidos WHERE session_id = $1";
        const checkResult = await pool.query(checkQuery, [session.id]);
        
        if (checkResult.rows.length > 0) {
          console.log(`üü° Webhook: Pedido ${session.id} j√° foi salvo. Ignorando.`);
          return res.json({ received: true });
        }
        // ---> FIM DA ADI√á√ÉO

        // üîπ Busca os itens
        const lineItems = await stripe.checkout.sessions.listLineItems(session.id, { limit: 100 });

        // üîπ Monta o pedido
        const pedido = {
          sessionId: session.id,
          email: session.customer_email || session.customer_details?.email, // <--- MELHORADO
          total: session.amount_total / 100,
          forma_pagamento: session.payment_method_types[0] || "indefinido",
          status: "pago",
          itens: lineItems.data.map((li) => ({
            descricao: li.description,
            quantidade: li.quantity,
            preco_unitario: li.price?.unit_amount ? li.price.unit_amount / 100 : 0,
          })),
        };

        console.log("‚úÖ Pedido recebido via webhook:", pedido);

        // üîπ Salva no banco
        try {
          const pedidoQuery = `
            INSERT INTO pedidos (session_id, customer_email, total, forma_pagamento, status)
            VALUES ($1, $2, $3, $4, $5)
            RETURNING id;
          `;
          const pedidoValues = [
            pedido.sessionId,
            pedido.email,
            pedido.total,
            pedido.forma_pagamento,
            pedido.status,
          ];

          const pedidoResult = await pool.query(pedidoQuery, pedidoValues);
          const pedidoId = pedidoResult.rows[0].id;

          for (const item of pedido.itens) {
            await pool.query(
              `
              INSERT INTO pedido_itens (pedido_id, descricao, quantidade, preco_unitario)
              VALUES ($1, $2, $3, $4);
              `,
              [pedidoId, item.descricao, item.quantidade, item.preco_unitario]
            );
          }

          console.log(`üíæ Pedido salvo no banco com ID: ${pedidoId}`);
        } catch (erroBanco) {
          console.error("‚ùå Erro ao salvar pedido no banco (webhook):", erroBanco);
        }
      }

      res.json({ received: true });
    } catch (err) {
      console.error("‚ö†Ô∏è Erro webhook:", err.message);
      res.status(400).send(`Webhook Error: ${err.message}`);
    }
  }
);

// =========================================================
// üîπ 2. MIDDLEWARES NORMAIS (vem DEPOIS do webhook)
